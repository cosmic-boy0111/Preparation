void dfs(int i, int j, int initalCol, int newCol, vector<vector<int>>& image){
    if(i < 0 || i >= image.size()) return;
    if(j < 0 || j >= image[0].size()) return;
    if(image[i][j] != initalCol) return;
    image[i][j] = newCol;
    dfs(i-1, j, initalCol, newCol, image);
    dfs(i+1, j, initalCol, newCol, image);
    dfs(i, j-1, initalCol, newCol, image);
    dfs(i, j+1, initalCol, newCol, image);
}

