class Node {
public:
    int val;
    Node* next;
    Node* random;
    
    Node(int _val) {
        val = _val;
        next = NULL;
        random = NULL;
    }
};

Node* copyRandomList(Node* head) {

    if(!head) return head;
    unordered_map<Node*, Node*> mp;

    Node* curr = head;
    Node* copy = new Node(head->val);

    mp[curr] = copy;
    while (curr->next){
        Node* next = curr->next;
        Node* next_copy = new Node(curr->next->val);

        copy->next = next_copy;
        mp[next] = next_copy;

        curr = curr->next;
        copy = copy->next;
    }

    for(auto &x : mp){
        if(x.first->random){
            x.second->random = mp[x.first->random];
        }
    }
    

    return mp[head];


}

