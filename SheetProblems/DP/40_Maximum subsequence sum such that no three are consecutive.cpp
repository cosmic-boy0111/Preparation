int solve(vector<int>& arr , int n , vector<int>& dp) {
    if(n < 0) return 0;
    if(n == 0) return arr[0];

    if(dp[n] != -1) return dp[n];

    int a = arr[n] + solve(arr,n-2,dp);
    int b = arr[n] + arr[n-1] + solve(arr,n-3,dp);
    int c = solve(arr,n-1,dp);

    return dp[n] = max({a,b,c});

}

int maxSum(vector<int> arr, int n){
    vector<int> dp(n+1,-1);
    return solve(arr,n-1,dp);

}

