class Node {
public:
    int val;
    vector<Node*> children;

    Node() {}

    Node(int _val) {
        val = _val;
    }

    Node(int _val, vector<Node*> _children) {
        val = _val;
        children = _children;
    }
};

vector<vector<int>> levelOrder(Node* root) {
    vector<vector<int>> ans;
    queue<Node*> q;
    q.push(root);
    q.push(NULL);
    vector<int> temp;
    while ( !q.empty() ){
        Node* n = q.front();
        q.pop();
        temp.push_back(n->val);
        for(auto &x : n->children){
            q.push(x);
        }
        if(q.front() == NULL){
            q.pop();
            ans.push_back(temp);
            temp = {};
            if(!q.empty()) q.push(NULL);
        }
    }

    return ans;
    
}
        
  


