vector <int> calculateSpan(int price[], int n){
    stack<pair<int,int>> st;
    vector<int> ans;
    ans.push_back(1);
    st.push({price[0],1});
    for(int i=1;i<n;i++){
        pair<int,int> curr =  {price[i],1};
        while ( !st.empty() and st.top().first <= curr.first ){
            curr = {price[i],curr.second + st.top().second};
            st.pop();
        }

        ans.push_back(curr.second);
        st.push(curr);
    }

    return ans;
}

