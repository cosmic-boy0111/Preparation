void solve(vector<string>& ans,string s,int i){
    if(i>= s.size()){
        ans.push_back(s);
        return;
    }

    for(int j = i; j < s.size();j++){
        if(j != i and s[j] == s[j-1]) continue;
        swap(s[j],s[i]);
        solve(ans,s,i+1);
        swap(s[j],s[i]);
    }
}

vector<string>find_permutation(string S){
    vector<string> ans;
    sort(S.begin(),S.end());
    solve(ans,S,0);
    sort(ans.begin(),ans.end());
    return ans;
}

